package main

import "fmt"

func main() {

	// Declaration
	var a int     // Defaults to 0
	var b string  // Default to empty string
	var c float64 // Defaults to 0
	var d bool    // Defaults to false

	// Shows the type of the value
	fmt.Printf("%v \n", a)
	fmt.Printf("%v \n", b)
	fmt.Printf("%v \n", c)
	fmt.Printf("%v \n", d)
}
